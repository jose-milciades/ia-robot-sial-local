package com.edi.ia.robotsial.modelo;

public class RespuestaCargaSialVO {

	
	private int idExpediente;
	private String urlExpedietne;
	private String procesoTerminado;
	private int idCargaDemanda;
	public int getIdExpediente() {
		return idExpediente;
	}
	public void setIdExpediente(int idExpediente) {
		this.idExpediente = idExpediente;
	}
	public String getUrlExpedietne() {
		return urlExpedietne;
	}
	public void setUrlExpedietne(String urlExpedietne) {
		this.urlExpedietne = urlExpedietne;
	}
	public String getProcesoTerminado() {
		return procesoTerminado;
	}
	public void setProcesoTerminado(String procesoTerminado) {
		this.procesoTerminado = procesoTerminado;
	}
	public int getIdCargaDemanda() {
		return idCargaDemanda;
	}
	public void setIdCargaDemanda(int idCargaDemanda) {
		this.idCargaDemanda = idCargaDemanda;
	}
	
}
